function getCategoryTypeIds(category: CategoryStructure): string[] {
  const typeIds: string[] = [];

  if (category.types) {
    typeIds.push(...category.types.map(t => t.id));
  }

  if (category.subcategories) {
    category.subcategories.forEach(sub => {
      typeIds.push(...getCategoryTypeIds(sub));
    });
  }

  return typeIds;
}

